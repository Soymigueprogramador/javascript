// Estructura if else
/*
    IF
    El if es un condicional que dependiendo de la condicion puede ejecutar una porcion de codigo o no.
    ELSE:
    El else ejecuta una porcion de codigo que no cumpla la condicion del if.
*/

// Ejemplo en codigo
let edad = prompt('Ingresa tu edad');
let mayorEdad = 18;

if( edad >= mayorEdad ) {
    console.log('Sos mayor de edad');
} else {
    console.log('No sos mayor de edad');
}