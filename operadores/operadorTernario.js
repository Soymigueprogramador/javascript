// Operador ternario
/*
    El operador ternario es similar al if pero lo podemos usar para mostrar una cosa o otra
    segun sea necesario.
*/

// Operadores:
/**
    * ? Se usa en caso de que el codigo siguiente sea verdadero
    * : Se usa en caso de que el codigo siguiente sea falso
 */

// Ejemplo en codigo:
let edad = 21;
let acceso = edad >= 18 ? 'Pasa bro' : 'No pasas bro';

console.log(acceso);