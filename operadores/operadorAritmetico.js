// Operadores aritmeticos
/*
    Los operadores aritmeticos son los operadores matematicos.
    Los podemos usar para cuando tenemos que hacer alguna operacion matematica.
*/

// Operadores:
/**
    * + Suma
    * - Resta
    * * Multiplicacion
    * / Divicion
    * % Modulo
    * ** Potencia
    * ++ Incrementar
    * -- Decrementar
 */

// Ejemplo en codigo.

// Esto es una declaracion.
let n1 = 4;
let n2 = 4;

// Esto es una expresion.
console.log(n1 + n2);
console.log(n1 - n2);
console.log(n1 * n2);
console.log(n1 / n2);
// Modulo % Muestra el sobrante de una division
console.log(n1 % n2);
// Potencia ** Muestra n1 elevado a n2
console.log(n1 ** n2);
// Incrementar ++ Suma 1 al valor de la variable.
console.log(n1++);
console.log(n1);
// Decrementar -- Resta 1 al valor de la variable.
console.log(n2--);
console.log(n2);