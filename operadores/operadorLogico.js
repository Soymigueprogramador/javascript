// Operadores logicos
/*
    Los operadores logicos son para comparar si los valores se cumplen.
*/

// Operadores:
/**
    * && and
    * || or
    * ! not
 */

// Ejemplo en codigo:
console.log('Operador and &&', true && true ); // Compara si mabas condiciones se cumplen (True)
console.log('Operador and &&', true && false); // Compara si mabas condiciones se cumplen (False)

console.log('Operador or ||', true || false); // Compara si una de las condiciones se cumplen (True)

console.log('Operador not !',  ! false); // Niega todas las condiciones

// Ejemplos de uso:
/**
    * Operador logico and (Cuando necesitamos que ambos datos sean iguales) usuario y contrase√±a
    * Operador logico or (Cuando necesitamos que solo uno de los datos sea igual) verificar la edad en una pagina web.
    * Opeador logico not (Cuando necesitamos negar los datos e invertirlos)
 */