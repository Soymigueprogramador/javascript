// Operador de comparacion
/*
    Los operadores de comparacion de usan para comparar variables entre si o contra
    otras cosas.
*/

// Operadores:
/**
    * < Mayor que
    * > Menor que
    * <= Mayor o igual
    * >= Menor o igual
    * == Igual a
    * != Distinto a
    * === Estrictamente igual a
    * !== Estrictamente distinto a
 */

// Ejemplo en codigo.

let a = 10;

console.log(a > 5);
console.log(a > 5);
console.log(a >= 5);
console.log(a <= 5);
console.log(a = 5);
console.log(a == 5);
console.log(a === 5);
console.log(a !== 5);
console.log(a != 5);