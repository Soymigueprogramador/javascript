// Diferencias entre declaracion y expresion.
/**
    * Declaracion:
        Las declaraciones se utilizan cuando se va a crear algo que se va a usar en un futuro.
        let, const, function

    * Expresion:
        Una expresion es una linea o bloque de codigo que evalue en un valor.
        4 + 10 = 14 Esto es evaluar en un valor.
        x = 4; Esto es una asignacion.
        miFuncion() Esto tambien es una expresion.
        Cada vez que tengamos un codigo y podamos sacar algo de el estamos ante una expresion.
        Si por el contrario estamos ante un codigo que no nos permite sacar nada de el estamos
        ante una declaracion.



 */