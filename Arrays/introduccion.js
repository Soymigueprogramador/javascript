// Introducción

// ¿Qué son los arrays?
/*
    Los arrays (o arreglos) son estructuras de datos que nos permiten almacenar
    varios valores en una sola variable. Cada valor se guarda en una posición
    o índice dentro del array.
*/

// ¿Para qué se usa un array?
/*
    Se utilizan para guardar y manejar listas de datos, como números, nombres,
    objetos, etc. Son muy útiles cuando necesitamos trabajar con colecciones
    de elementos relacionados.
*/

// Ejemplo en código
const arrayNumeros = [1, 2, 3, 4, 5];
console.log(arrayNumeros);