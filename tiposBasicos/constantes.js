// Esto es una variable.
let nombre = 'Miguel';
nombre = 'Anto';

// ¿Que es una constante?
/*
    Una constante es un tipo de variable pero que su valor no puede ser cambiado.
*/

// Ejemplo en codigo:
const apellido = 'Salazar';

console.log(nombre);
console.log(apellido);

// ¿Cuando debemos usar const o let?
/*
    Por regla general no deberiamos de poder cambiar los valores de una variable asi que generalmente
    deberiamos usar const.
    Solo debemos usar let en casos donde necesitemos cambiar si o si el valor de una variable.
*/